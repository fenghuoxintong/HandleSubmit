package com.bjym.mobiledata.busbean;

import java.util.List;

public class ReportResp {
	private String type;
	private int size;
	private List<ReportMessage> messageList;

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public List<ReportMessage> getMessageList() {
		return messageList;
	}

	public void setMessageList(List<ReportMessage> messageList) {
		this.messageList = messageList;
	}

	public int getSize() {
		return size;
	}

	public void setSize(int size) {
		this.size = size;
	}

}
